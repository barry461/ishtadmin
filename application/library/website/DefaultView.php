<?php

namespace website;

use Illuminate\Support\Str;
use Yaf\Application;
use Yaf\Exception\TypeError;
use Yaf\Registry;
use Yaf\View\Simple;
use Yaf\View_Interface;

class DefaultView implements View_Interface
{

    protected $theme = null;
    public $fields = null;

    /**
     * @throws TypeError
     */
    public static function newInstance($theme): DefaultView
    {
        $dir = sprintf("%s/themes/%s/", APP_PATH, $theme);
        $that = new self($dir);
        $that->fields = new \MyArrayObject([]);
        $that->theme = $theme;
        Registry::set("_view" , $that);
        return $that;
    }

    public function css($href)
    {
        if (!Str::endsWith($href , '.css')){
            $href .= '.css';
        }
        echo <<<HTML
<link rel="stylesheet" type="text/css" href="/themes/{$this->theme}/static/css/$href">
HTML;
    }

    public function static_dir($dir){

        echo "/themes/{$this->theme}/static/{$dir}/";
    }

  

    public function script($src , $attr = '')
    {
        if (!Str::endsWith($src , '.js')){
            $src .= '.js';
        }
        echo <<<HTML
<script type="text/javascript" $attr src="/themes/{$this->theme}/static/js/$src"></script>
HTML;

    }

    public function itemprop($src , $attr = '')
    {
        if (!Str::endsWith($src , '.png')){
            $src .= '.png';
        }
        return <<<HTML
<meta itemprop="url" content="/themes/{$this->theme}/static/Mirages/images/{$src}">
HTML;

    }

    public function image($src)
    {

        if (!preg_match('/\.(png|jpg|jpeg|gif|webp|svg)(\?|$)/i', $src)) {
            $src .= '.png';
        }

        echo "/themes/{$this->theme}/static/Mirages/images/{$src}";
    }

    public function render($tpl, $tpl_vars = null)
    {
        if (!Str::endsWith($tpl , '.phtml')){
            $tpl .= ".phtml";
        }
        return parent::render($tpl , $tpl_vars);
    }

    public function display($tpl, $tpl_vars = null)
    {
        if (!Str::endsWith($tpl , '.phtml')){
            $tpl .= ".phtml";
        }
        parent::display($tpl, $tpl_vars); // TODO: Change the autogenerated stub
    }


    public function footer()
    {
        $plugins = options('plugins');
        print_r($plugins);
        ob_flush();
        exit();
    }

    public function is(string $string)
    {
        $request = Application::app()->getDispatcher()->getRequest();
        $handle = $request->getModuleName() .'/' . $request->getControllerName() . '@' . $request->getActionName();
        if (str_contains($string , '@') || str_contains($string  ,'/')){
            return str_contains($handle, $string);
        }
        return $_SERVER['REQUEST_URI'] == $string;
    }

    public function vClass(array $map, string $prepend = ''): string
    {
        $classList = array_keys(array_filter($map));
        if ($prepend) {
            $classList[] = $prepend;
        }
        $classList = array_filter($classList); // 防止空项
        if (empty($classList)) {
            return '';
        }
        return htmlspecialchars(implode(' ', $classList));
    }

