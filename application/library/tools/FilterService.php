    private static function getPinyin(): ?Pinyin
    {
        static $pinyin = NULL;
        if ($pinyin === NULL) {
            $pinyin = new Pinyin();
        }
        return $pinyin;
    }

    public static function validate_include($txt): bool
    {
        foreach (self::INCLUDE_CHARS as $char) {
            if (strpos($txt, $char) !== false) {
                return true;
            }
        }
        $pinyin = self::getPinyin();
        $py = $pinyin->convert($txt);
        $rs = array_intersect($py, self::INCLUDE_PY_CHARS);
        return count($rs) > 0;
    }

    public static function validate_font($txt): bool
    {
        $txt = html_entity_decode($txt);
        $permitPregs = self::PERMIT_PREGS;

        $lastStr = trim(preg_replace($permitPregs, '', $txt));
        $len = strlen($lastStr);
        if ($len > 0) {
            return false;
        }

        $notPermitedPregs = self::NOT_PERMIT_PREGS;
        foreach ($notPermitedPregs as $preg) {
            $pregCount = preg_match($preg, $txt);
            if ($pregCount)
                return false;
        }
        return true;
    }

    // дёҖдәӣеҲ·иҜ„и®әзҡ„еһғеңҫ
    public static function include_special_char($txt): bool
    {
        $txt = json_encode($txt);
        foreach (self::NOT_ALLOW_JSON_CHARS as $v) {
            if (strpos($txt, $v) !== false) {
                return true;
            }
        }
        return false;
    }

    public static function parser_txt($txt)
    {
        $txt = html_entity_decode($txt);
        return str_replace(array_keys(self::REPLACE_CHARS), array_values(self::REPLACE_CHARS), $txt);
    }

    public static function validate($txt): bool
    {
        if (mb_strlen($txt) <= 3) {
            return true;
        }

        $pinyin = self::getPinyin();
        $nums = [];
        $pinyins = [];
        $letters = [];
        $i = mb_strlen($txt);
        for ($j = 0; $j < $i; $j++) {
            $cur = mb_substr($txt, $j, 1);
            if (in_array($cur, self::NUMBER_CHARS)) {
                $nums[] = $cur;
                continue;
            }
            $py = $pinyin->convert($cur);
            if ($py && in_array($py[0], self::PINYIN_CHARS)) {
                $pinyins[] = $cur;
                continue;
            }
            $cur = strtoupper($cur);
            if (in_array($cur, self::LETTER_CHARS)) {
                $letters[] = $cur;
            }
        }
        $nums = array_unique($nums);
        $pinyins = array_unique($pinyins);
        $letters = array_unique($letters);
        $all = array_unique(array_merge($nums, $pinyins, $letters));
        if (count($nums) > 3 || count($pinyins) > 3 || count($letters) > 3 || count($all) >= 3) {
            return false;
        }
        return true;
    }
